int	ft_is_prime(int nb)
{
	int	i;
	int	check;

	i = 2;
	check = 1;
	if (nb <= 1)
		return (0);
	while (i <= nb / 2)
	{
		if (nb % i == 0)
		{
			check = 0;
		}
		i++;
	}
	return (check);
}
